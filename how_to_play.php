			<h4>Introduction</h4>
			<div id="introduction">
				<p>
					This is ideally a 4 player game in which each player plays to reach a set target of 300. The game is played in multiple sets. For each set players score some points to help them move towards the target. The score can be positive or negative for each player.
				</p>
				<p>
					So, the game is played in sets unless someone's total score crosses the target. Set is a collection of 13 hands played after each deal as the players are dealt 13 cards each (13 x 4 = 52). After each deal, all players make a call on how many hands they can secure in the game. After the set is complete the score is given to players depending on whether they secured more than, less than or equal to the call they had made. A sum of scores after each set gives the player's total score.
				</p>
			</div>

			
			<h4>Pre-requisite</h4>
			<div id="pre_requisite">
				<p>
					<ol>
						<li>A deck of 52 cards</li>
						<li>A pen and paper to note down the scores</li>
						<li>4 willing players who know the rules of game</li>
					</ol>
				</p>
			</div>
			
			<h4>Rules</h4>
			<div id="rules">
				<p>
					<ol>
						<li>There are 4 players in game they form a circle.</li>
						<li>There is a dealer for each set and the deal rotates and also the game is played anti-clockwise.</li>
						<li>The person next to the dealer (in anti-clockwise order) starts making call on hands he can take.</li>
						<li>After the deal if any of the following conditions are met then the cards are dealt again by the next person (in anti-clockwise order). If any one person has all the four aces OR If any one person has none of the trump card (explained later) OR If any one person has cards such that he doesn't have at least one of the four (Aces, Kings and Queens).
						</li>
						<li>Sum of calls made by all players before start of the set is less than nine.</li>
						<li>Order or priority of cards are natural i.e. 2, 3, 4, 5, 6, 7, 8, 9, 10, Jack, Queen, King, Ace in ascending order.</li>
						<li>Trump card is a suite of card (Spades / Hearts / Diamonds / Clubs) which has higher priority to all other suites. For example if Spade is trump card then a 2 of spade has higher priority than an Ace of Clubs, Diamonds or Hearts.</li>
						<li>A player has to play the card of the suite for which the hand was started, if he has cards of that suite otherwise he has to play trump card if he has trump cards.</li>
						<li>A player has to compulsorily play a higher value/priority card than the cards already played in the hand, provided he has one. If the player doesn't have a higher value card then he can play any card keeping in mind rule 7.</li>
						<li>The trump card can be fixed or decided for each set randomly. If you decide to have a fixed trump, it is a general practice to use spade as the trump card. If you decide to choose the trump at random then after the set is dealt the dealer shows everyone the last card dealt and that suite becomes the trump for the set.</li>
						<li>
							After one set of 13 hands is finised this is how scores are assigned,
							<ul>
								<li>If the player has secured exactly the number of hands he had claimed at the start of the set. He is given points 10 times that of call he had made. E.g. if the call was of 4 hands and 4 hands were secured. Then the points given would be 40.</li>
								<li>If the player couldn't secure the number of hands he had claimed he would. He is given negative points 10 times that of call he had made. E.g. if the call was of 4 hands and less than 4 hands were secured. Then the points given would be -40.</li>
								<li>If the player has secured more hands than he had claimed. He is given bonus points which is computed as follows. If the player had called X hands and secured (X + 2) hands. Then the points given would be (10X + 2). E.g. if the call was of 4 and 6 hands were secured. Then the points given would be 42.</li>
							</ul>
						</li>
					</ol>

				</p>
			</div>

			<h4>How to Play</h4>
			<div id="how_to_play">
				<p>
					<ol>
						<li>Ideally for each of maintaing scores a table is maintained in which each column is for one player and each row has score of one set. The columns are assigned to the players sitting in anti-clockwise order. This makes it easy to maintain & total scores.</li>
						<li>For the first time dealer can be picked at random of by consensus, it doesn't matter much.</li>
						<li>The dealer shuffles and deals the cards to all four players dealing one card at a time and rotating anti-clockwise. He must get the last card which could be decided as trump if the game is of changing trump.</li>
						<li>Now every one decides the number of hands he/she can secure depending on how many Aces, Kings, Queens and trump cards he/she has got. As there are total 13 cards of each suite. There may be a maximum of 3 hands of one suite without anyone using the trump card. Generally people get cards in which some body will trump in the third hand of a single suite. So, Making calls on Ace & King may be safe but may not be that safe on queen. On the other hand if you are trumping any suite 1st, 2nd or 3rd hand you may accordingly secure 3, 2 or 1 hand by trumping that suite, provided you have enough trump cards.</li>
						<li>One also has to take in account that the person behind you can and probably will try to cut down your hands in order to secure his hands or simply to cut off your hands so that you go negative. For example: if you have a King and a 10 of club and the person behind you starts the hand with a Jack of club, now because of rule 8 you are forced to play your king of club and the hand eventually goes to someone next to you with the Ace of club. So, you lost your King and some one's Queen will win the hand next time.</li>
						<li>Also, suppose you are trumping hearts from 2nd hand and you assume you will secure at least 2 hands by trumping it. But when you trump once you opponent starts playing game of trumps and consequently your trump cards are finished. Now he will be able to secure his hands of hearts and you won't be able to trump again as you run out of trump cards. Then you will probably get a negative score.</li>
						<li>After the set is finished the hands secured by everyone is counted and accordingly the scores are given.</li>
						<li>Now the deal goes to the next person to the dealer of last set in anti-clockwise order.</li>
						<li>If some one is caught of cheating the rules of the game, that set is declared void and the cheater's highest score on the game so far is selected and made negative.</li>
						<li>If the set can not be played because the total of calls was less than 9. The cards of the last person making the call (that is the dealer himself) are checked to see if he was purposely hiding the calls to cancel the set. If it is so the same penalty as cheating is given to him.</li>
						<li>Game progresses this way until one of the players cross the target score and then that player is declared winner.</li>
					</ol>
				</p>
			</div>

			<h4>Further Note</h4>
			<div id="further_note">
				<p>
					The games becomes interesting because of the fact that you can force others to play a certain way so that they might not secure their call and get a negative score pushing you ahead in the game. This is possible because of rule 7 & 8. That is one has to play a higher value card if he has one and one has to trump a suite if he doesn't have one. This is one of my favorite games of card. So, if some one wants a demo they are welcome to my place.
				</p>
